CREATE PROCEDURE SP_SIGN_IN_NHANVIEN
	@TENDN NVARCHAR(100),
	@MK NVARCHAR(100)
AS
BEGIN
	DECLARE @HASHED_MK VARBINARY(MAX)
	SET @HASHED_MK = HASHBYTES('SHA1', CONVERT(VARBINARY(MAX), @MK))

	IF NOT EXISTS (SELECT 1 FROM NHANVIEN WHERE TENDN = @TENDN AND MATKHAU = @HASHED_MK)
	BEGIN
		-- RAISERROR('Tài khoản nhân viên với TENDN: %s và MK: %s không tồn tại', 16, 1, @TENDN, @MK)
		SELECT 0
		RETURN
	END

	SELECT 1
	RETURN
END
